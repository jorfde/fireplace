import SwiftUI

struct PixelText: View {
    let text: String
    var pixelSize: CGFloat = 2
    var color: Color = .primary
    var opacity: Double = 1.0

    var body: some View {
        Canvas { ctx, size in
            let chars = Array(text.uppercased())
            let charW = 5
            let charH = 7
            let spacing = 1
            let totalW = chars.count * (charW + spacing) - spacing
            let startX = (size.width - CGFloat(totalW) * pixelSize) / 2
            let startY = (size.height - CGFloat(charH) * pixelSize) / 2

            for (ci, char) in chars.enumerated() {
                guard let glyph = PixelFont.glyphs[char] else { continue }
                let ox = startX + CGFloat(ci * (charW + spacing)) * pixelSize

                for row in 0..<charH {
                    for col in 0..<charW {
                        if glyph[row * charW + col] {
                            let rect = CGRect(
                                x: ox + CGFloat(col) * pixelSize,
                                y: startY + CGFloat(row) * pixelSize,
                                width: pixelSize,
                                height: pixelSize
                            )
                            ctx.fill(Path(rect), with: .color(color.opacity(opacity)))
                        }
                    }
                }
            }
        }
        .frame(
            width: CGFloat(text.count * 6) * pixelSize,
            height: 7 * pixelSize
        )
    }
}

